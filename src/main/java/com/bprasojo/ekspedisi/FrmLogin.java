    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = edUser.getText();
            String password = new String(edPassword.getPassword());

            try {
                UserDAO userDAO = new UserDAO();
                User user = userDAO.login(username, password);

                if (user != null) {
//                    JOptionPane.showMessageDialog(FrmLogin.this, "Selamat datang, " + user.getFullname());
                    MainForm mainForm = ((MainForm) this.getOwner());
                    mainForm.user = user;
                    mainForm.setMenuItemsEnabled(true);
                    mainForm.SetCaptionMenuLogin("Logout");
                    dispose(); // Tutup login setelah berhasil
                } else {
                    JOptionPane.showMessageDialog(FrmLogin.this, "Login gagal. Periksa username dan password!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmLogin.class.getName()).log(Level.SEVERE, "Kesalahan Database", ex);
                AppUtils.showErrorDialog("Terjadi kesalahan saat mengakses database.");
            }
    }//GEN-LAST:event_btnLoginActionPerformed

